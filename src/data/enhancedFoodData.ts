// Generate more variations for infinite scroll
export const generateMoreFoods = (startIndex: number, count: number): Food[] => {
  const generated: Food[] = [];
  for (let i = 0; i < count; i++) {
    const baseFood = ENHANCED_FOODS[i % ENHANCED_FOODS.length];
    generated.push({
      ...baseFood,
      id: `${baseFood.id}-gen-${startIndex + i}`,
      name: `${baseFood.name} ${Math.floor(startIndex / ENHANCED_FOODS.length) + 1}`,
    });
  }
  return generated;
};

