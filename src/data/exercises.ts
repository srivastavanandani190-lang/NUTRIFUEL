// Helper function to get exercises by category
export const getExercisesByCategory = (category: string): Exercise[] => {
  return ALL_EXERCISES.filter(ex => ex.category === category);
};

// Helper function to calculate total calories for an exercise
export const calculateExerciseCalories = (exercise: Exercise): number => {
  return exercise.caloriesPerMin * exercise.duration;
};
